    public EditQuestionFrame(Exam editExam)
    {
        this();
        editExamId=editExam;
        lblShowPaperName.setText("EDITING "+editExam.getLanguage()+" PAPER");
        loadQuestion();
        showQuestion();
    }
    private void loadQuestion()
    {
        try
        {
            ArrayList<Question> questionList=QuestionDao.getQuestionByExamId(editExamId.getExamId());
            for(Question q:questionList)
            {
                
                Store.addQuestion(q);
            }
            System.out.println(questionList);
        }
        catch(Exception ex)
            {
              JOptionPane.showMessageDialog(null,"DB Error","Edit Ques Error",JOptionPane.ERROR_MESSAGE);
              ex.printStackTrace();
            }
        
    }
    private void showQuestion()
    {
       Question q=Store.getQuestion(pos);
       txtShowQuestion.setText(q.getQuestion());
       txtOption1.setText(q.getAnswer1());
       txtOption2.setText(q.getAnswer2());
       txtOption3.setText(q.getAnswer3());
       txtOption4.setText(q.getAnswer4());
       String correctAns=q.getCorrectAnswer();
       String correctOpt=getKeyFromValue(correctAns);
       jcbCorrectOption.setSelectedItem(correctOpt);
    }
    private String getKeyFromValue(String value)
    {
        Set<String> keys=option.keySet();
        for(String k:keys)
        {
            String v=option.get(k);
            if(v.equals(value))
                return k;
        }
        return null;
    }

