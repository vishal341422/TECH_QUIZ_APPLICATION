    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        boolean result=validateInput();
        if(!result)
        {
           JOptionPane.showMessageDialog(null,"Please fill all the fields","Error",JOptionPane.ERROR_MESSAGE);
           return;
           
        }
         String optionName=option.get(correctOption);
         Question obj=new Question(editExamId.getExamId(),qno,editExamId.getLanguage(),option1,option2,option3,option4,optionName,question);
         Store.removeQuestion(pos);
         Store.setQuestionAt(pos, obj);
         pos++;
         if(pos>=editExamId.getTotalQuestion())
         {
             System.out.println(Store);
             JOptionPane.showMessageDialog(null,"Your Question have been edited successfully.\nPress Done Button to added Question into DB","Editing Exam",JOptionPane.INFORMATION_MESSAGE);
             pos=0;
         }
         showQuestion();
         qno++;
         if(qno>editExamId.getTotalQuestion())
         {
             qno=1;
         }
         lblShowQuesNo.setText("Question No:"+qno);
    }//GEN-LAST:event_btnNextActionPerformed

    private void btnPreviousActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPreviousActionPerformed
           boolean result=validateInput();
        if(!result)
        {
           JOptionPane.showMessageDialog(null,"Please fill all the fields","Error",JOptionPane.ERROR_MESSAGE);
           return;
           
        }
         String optionName=option.get(correctOption);
         Question obj=new Question(editExamId.getExamId(),qno,editExamId.getLanguage(),option1,option2,option3,option4,optionName,question);
         Store.removeQuestion(pos);
         Store.setQuestionAt(pos, obj);
         pos--;
         if(pos<0)
         {
             JOptionPane.showMessageDialog(null,"Your Question have been edited successfully.\nPress Done Button to added Question into DB","Editing Exam",JOptionPane.INFORMATION_MESSAGE);
             pos=editExamId.getTotalQuestion()-1;
         }
         showQuestion();
         qno--;
         if(qno<=0)
         {
             qno=editExamId.getTotalQuestion();
         }
         lblShowQuesNo.setText("Question No:"+qno);
    }//GEN-LAST:event_btnPreviousActionPerformed

