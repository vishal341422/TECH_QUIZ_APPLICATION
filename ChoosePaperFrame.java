    private void jcbChooseSubjectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcbChooseSubjectActionPerformed
             jcbChooseExamId.removeAllItems();
        boolean result=validateInput();
        if(!result)
        {
            JOptionPane.showMessageDialog(null,"Please select a subject","Choose subject",JOptionPane.ERROR_MESSAGE);
            return;
        }
        try
        {
            if(ExamDao.isPaperSet(subjectNames)==false)
            {
                JOptionPane.showMessageDialog(null,"Sorry! No Exam set for this paper "+subjectNames,"Sorry!",JOptionPane.INFORMATION_MESSAGE);
                return; 
            }
            ArrayList<String>examList=ExamDao.getExamIdBySubject(UserProfile.getUsername(), subjectNames);
            if(examList.isEmpty())
            {
               JOptionPane.showMessageDialog(null,"Sorry! No Exam Found "+subjectNames,"Sorry! ",JOptionPane.INFORMATION_MESSAGE);
                return; 
            }
            for(String examId:examList)
            {
                jcbChooseExamId.addItem(examId);
            }
        }
         catch(Exception ex)
        {
          JOptionPane.showMessageDialog(null,"DB Error!","Choose Paper Frame Error!",JOptionPane.ERROR_MESSAGE);  
          ex.printStackTrace();
        } 
    }//GEN-LAST:event_jcbChooseSubjectActionPerformed

