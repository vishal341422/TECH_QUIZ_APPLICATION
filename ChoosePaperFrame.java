    private void btnTakeTestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTakeTestActionPerformed
        boolean result=validateInput();
        if(!result)
        {
            JOptionPane.showMessageDialog(null,"Please select a subject","Choose subject",JOptionPane.ERROR_MESSAGE);
            return;
        }
        int count=jcbChooseExamId.getItemCount();
        if(count==0)
        {
            JOptionPane.showMessageDialog(null,"Sorry! please select exam id ","Sorry!",JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        String examId=jcbChooseExamId.getSelectedItem().toString();
        int ans=JOptionPane.showConfirmDialog(null,"You have selected "+subjectNames+" and "+examId+" is this ok ?","Are you sure !",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        if(ans==JOptionPane.YES_OPTION)
        {
            try
            {
                int totalQues=ExamDao.getQuestionCountByExam(examId);
                Exam exam=new Exam(examId,subjectNames,totalQues);
                takeTestFrame fr=new takeTestFrame(exam);
                fr.setVisible(true);
                this.dispose();
                
            }
            catch(Exception ex)
           {
               JOptionPane.showMessageDialog(null,"DB Error!","Choose Paper Frame Error!",JOptionPane.ERROR_MESSAGE);  
               ex.printStackTrace();
            } 
        }
    }//GEN-LAST:event_btnTakeTestActionPerformed

